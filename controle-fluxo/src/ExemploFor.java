public class ExemploFor {
    public static void main(String[] args) {

        /*
         * Laços de repetição, também conhecidos como laços de iteração ou simplesmente
         * loops, são comandos que permitem iteração de código, ou seja, que comandos
         * presentes no bloco sejam repetidos diversas vezes.
         */

        for (int carneirinho = 1; carneirinho <= 20; carneirinho++) {
            System.out.println("Contando carneirinhos " + carneirinho);
        }
        System.out.println("Joãozinho dormiu");
    }
}
