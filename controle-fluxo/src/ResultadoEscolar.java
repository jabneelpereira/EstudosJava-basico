public class ResultadoEscolar {
    public static void main(String[] args) {
        int nota = 8;
        /*
         * A Estrutura Condicional, possibilita a escolha de um grupo de ações e
         * comportamentos a serem executadas. Nesse caso, o if só será executado se
         * sua condição for verdadeira, caso contrario else e quem sera executado.
         */
        if (nota >= 7)
            System.out.println("Aprovado");

        else
            System.out.println("Reprovado");
    }
}
